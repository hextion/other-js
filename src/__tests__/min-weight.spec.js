const { minWeight } = require("../min-weight");

test("minWeight works fine", () => {
  expect(
    minWeight([
      [1, 1, 1, 1, 1],
      [3, 100, 100, 100, 100],
      [1, 1, 1, 1, 1],
      [2, 2, 2, 2, 1],
      [1, 1, 1, 1, 1],
    ])
  ).toBe(11);
});

